  <p><h3>PREPARE NOW!</h3></p>
  <ul> <li>Know types of flood risk in your area. Visit FEMA’s Flood Map Service Center for information. </li></ul>
    <ul> <li> Sign up for your community’s warning system. The Emergency Alert System (EAS) and National Oceanic and Atmospheric Administration (NOAA) Weather Radio also provide emergency alerts.</li></ul>
	<ul> <li>If flash flooding is a risk in your location, then monitor potential signs, such as heavy rain. </li></ul>
	<ul> <li>Learn and practice evacuation routes, shelter plans, and flash flood response. </li></ul>
	<ul> <li> Gather supplies in case you have to leave immediately, or if services are cut off. Keep in mind each person’s specific needs, including medication. Don’t forget the needs of pets. Obtain extra batteries and charging devices for phones and other critical equipment.</li></ul>
	<ul> <li> Keep important documents in a waterproof container. Create password-protected digital copies.</li></ul>
