<h1 style="margin-top:40px;font-size:65px;margin-left:40px;color:white">FLOODS</h1>
<p style="margin-left:40px; color: white; font-size: 25px ">Failing to evacuate flooded areas, entering flood waters, or remaining after a flood 
has passed can result in injury or death.
 Flooding is a temporary overflow of water onto land that is normally dry. Floods
 are the most common natural disaster in the United States. Floods may:</p>
 <ul type="disc">
 <li>Result from rain, snow, coastal storms, storm surges, and overflows of dams and other water systems.</li>
 <li>Develop slowly or quickly – Flash floods can come with no warning.</li>
 <li>Cause outages, disrupt transportation, damage buildings, and create landslides.</li>
 </ul>
 <p style="font-weight: bold; margin-left: 6em;"> IF YOU ARE UNDER A FLOOD WARNING, FIND SAFE SHELTER RIGHT AWAY</p>
  <ul> <li> Do not walk, swim, or drive through flood waters. Turn Around, Don’t Drown!</li></ul>
  <ul> <li>Just six inches of moving water can knock you down, and one foot of moving water can sweep your vehicle away. </li></ul>
  <ul> <li>Stay off of bridges over fast-moving water. </li></ul>
  <ul> <li> Determine how best to protect yourself based on the type of flooding.</li></ul>
