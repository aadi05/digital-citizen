<h1 style="margin-top:40px;font-size:65px;color:white;margin-left:40px">HURRICANES</h1>
<p style="margin-left:40px; color: white; font-size: 25px ">Hurricanes are massive storm systems that form over warm ocean waters and move toward land. Potential threats from hurricanes include powerful winds, heavy rainfall, storm surges, coastal and inland flooding, rip currents, tornadoes, and landslides. The Atlantic hurricane season runs from June 1 to November 30. The Pacific hurricane season runs May 15 to November 30. Hurricanes:</p>
 <ul type="disc">
 <li>Can happen along any U.S. coast or in any territory in the Atlantic or Pacific oceans.
</li>
 <li>Can affect areas more than 100 miles inland.
</li>
 <li>Are most active in September.
</li>
 </ul>
 <p style="font-weight: bold; margin-left: 6em;"> IF YOU ARE UNDER A HURRICANE WARNING, FIND SAFE SHELTER RIGHT AWAY
</p>
  <ul><li>Determine how best to protect yourself from high winds and flooding.<ul style="list-style-type: circle;"><li>Evacuate if told to do so.</li><li>Take refuge in a designated storm shelter, or an interior room for high winds.</li></ul></li><li>Listen for emergency information and alerts.</li><li>Only use generators outdoors and away from windows.</li><li>Turn Around, Don’t Drown! Do not walk, swim, or drive through flood waters.</li></ul>
  

