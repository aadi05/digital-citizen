  <p><h1>PREPARE NOW!</h1></p>
  <ul><li>Know your area’s risk of hurricanes.</li><li> The Emergency Alert System (EAS) and National Oceanic and Atmospheric Administration (NOAA) Weather Radio also provide emergency alerts.</li><li>If you are at risk for flash flooding, watch for warning signs such as heavy rain.</li><li>Practice going to a safe shelter for high winds, such as a FEMA safe room or ICC 500 storm shelter. The next best protection is a small, interior, windowless room in a sturdy building on the lowest level that is not subject to flooding.</li><li>Based on your location and community plans, make your own plans for evacuation or sheltering in place.</li><li>Become familiar with your evacuation zone, the evacuation route, and shelter locations.</li><li>Gather needed supplies for at least three days. Keep in mind each person’s specific needs, including medication. Don’t forget the needs of pets.</li><li>Keep important documents in a safe place or create password-protected digital copies.</li><li>Protect your property. Declutter drains and gutters. Install check valves in plumbing to prevent backups. Consider hurricane shutters. Review insurance policies.</li></ul></body>
